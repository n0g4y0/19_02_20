public class Componente
{
    private int digito;
    private int posicion;
   
    public Componente(int digito, int posicion){
        
        this.digito = digito;
        this.posicion = posicion;
    }
    
    public int getDigito(){
        
        return digito;
    }
    
    public int getPosicion(){
        return posicion;
    }
}
